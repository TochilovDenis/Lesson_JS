/*Задание: 
1 Создать массив имен
2 Создать функцию которая принимает остаточные параметры и выводит в консоль "Имя + {длина имени}"
3 вызвать функцию передав в нее массив с оператором расширения
*/

// 1. Создаем массив имен
const names = ["Denis", "Vasya", "Ivan"];
// 2. Создаем функцию с остаточными параметрами
function printNamesWithLength(...names) 
{   // Используем for...of для перебора всех переданных имен
    for (const name of names) 
        {        
        alert(`${name} + {${name.length}}`);// Выводим в консоль имя и его длину
    }
}
// 3. Вызываем функцию с оператором расширения
printNamesWithLength(...names);

/*Массив имен создается с помощью квадратных скобок и содержит строки с именами
Функция с остаточными параметрами:
Оператор ... (rest parameters) позволяет функции принимать любое количество аргументов
Все переданные аргументы собираются в массив names внутри функции
Используем цикл for...of для перебора всех элементов
${} - шаблонная строка для форматирования вывода
name.length возвращает длину строки
Оператор расширения ... при вызове функции:
Распаковывает массив names на отдельные аргументы
Каждый элемент массива передается как отдельный параметр в функцию
Такой подход позволяет гибко работать с любым количеством имен, передавая их в функцию.*/


function name_l(...mas) {
    for(let agr of mas){
        console.log(agr + " - " + agr.length + "\n")
    }
}

const names1 = ["den", "sveta", "sasha"]

name_l(...names1)